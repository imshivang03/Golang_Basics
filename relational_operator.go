package main 
   
import "fmt"
   
func main() { 
   p:= 34
   q:= 20
       
   result1:= p == q 
   fmt.Println(result1) 
      
   result2:= p != q 
   fmt.Println(result2) 
      
   result3:= p < q 
   fmt.Println(result3) 
      
   result4:= p > q 
   fmt.Println(result4) 
      
   result5:= p >= q 
   fmt.Println(result5) 
       
   result6:= p <= q 
   fmt.Println(result6) 
      
      
} 
